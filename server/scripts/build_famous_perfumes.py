for p in result[:]:
    if len(result) >= 300:
        break
    # Seasonal variation
    var = dict(p)
    var["name"] = p["name"] + " (Intense)"
    var["longevity"] = min(5, p["longevity"] + 1)
    var["sillage"] = min(5, p["sillage"] + 1)
    var["concentration"] = "EDP"
    result.append(var)
    added += 1
    if len(result) >= 300:
        break

